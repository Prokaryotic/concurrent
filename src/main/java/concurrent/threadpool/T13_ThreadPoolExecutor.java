public class T13_ThreadPoolExecutor {


//    ThreadPoolExecutor
//    public ThreadPoolExecutor(int corePoolSize,
//                              int maximumPoolSize,
//                              long keepAliveTime,
//                              TimeUnit unit,
//                              BlockingQueue<Runnable> workQueue,
//                              ThreadFactory threadFactory)用给定的初始参数和默认被拒绝的执行处理程序创建新的 ThreadPoolExecutor。
//
//    参数：
//    corePoolSize - 池中所保存的线程数，包括空闲线程。
//    maximumPoolSize - 池中允许的最大线程数。
//    keepAliveTime - 当线程数大于核心时，此为终止前多余的空闲线程等待新任务的最长时间。
//    unit - keepAliveTime 参数的时间单位。
//    workQueue - 执行前用于保持任务的队列。此队列仅保持由 execute 方法提交的 Runnable 任务。
//    threadFactory - 执行程序创建新线程时使用的工厂。
//    抛出：
//    IllegalArgumentException - 如果 corePoolSize 或 keepAliveTime 小于 0，或者 maximumPoolSize 小于等于 0，或者 corePoolSize 大于 maximumPoolSize。
//    NullPointerException - 如果 workQueue 或 threadFactory 为 null。

}
